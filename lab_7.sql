--9.Create trigger to Allow enrolment in month from Jan to August, otherwise print message enrolment closed.
CREATE OR ALTER TRIGGER TR_ENROOMENT_MESSAGE
ON ENROLLMENT
INSTEAD OF INSERT
AS
BEGIN
	DECLARE @EMONTH INT
	SELECT @EMONTH = MONTH(EnrollmentDate) FROM INSERTED
	IF(@EMONTH = 1 OR @EMONTH = 2 OR @EMONTH = 3 OR @EMONTH = 4 OR @EMONTH = 5 OR @EMONTH = 6 OR @EMONTH = 7 OR @EMONTH = 8)
	BEGIN
		INSERT INTO ENROLLMENT(STUDENTID,COURSEID,ENROLLMENTDATE,GRADE,ENROLLMENTSTATUS)
		SELECT STUDENTID,COURSEID,ENROLLMENTDATE,GRADE,ENROLLMENTSTATUS FROM INSERTED
	END
ELSE
		BEGIN
		PRINT('ERROR:DUPLICATE NAME AN FACULTY WITH THIS NAME ALREADY EXITS')
		END
END
