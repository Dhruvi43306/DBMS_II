CREATE OR ALTER TRIGGER TR_DUPLICATE_FACULTY
ON FACULTY
INSTEAD OF INSERT
AS
BEGIN
	IF EXISTS(SELECT 1
			FROM FACULTY F
			JOIN INSERTED I 
			ON F.FacultyEmail = I.FacultyEmail)
	BEGIN
		PRINT('ERROR:DUPLICATE NAME AN FACULTY WITH THIS NAME ALREADY EXITS')
		END
ELSE
	BEGIN
		INSERT INTO FACULTY(FacultyID,FacultyName,FacultyEmail,FacultyDepartment,FacultyDesignation,FacultyJoiningDate)
		SELECT FacultyID,FacultyName,FacultyEmail,FacultyDepartment,FacultyDesignation,FacultyJoiningDate FROM INSERTED
	END
END
